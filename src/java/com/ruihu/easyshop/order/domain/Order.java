package com.ruihu.easyshop.order.domain;
import java.util.List;
import com.ruihu.easyshop.user.domain.User;

public class Order {
	private String oid; 
	private String ordertime;//Oder time
	private double total;//totally payment
	private int status;//Order Statusï¼š1unpay, 2pay aready but not deliver, 3deliver but not confirm, 4confirm and suceess, 5cancel(only use in unpay)
	private String address;//user address   
	private User owner;//oder owner
	
	private List<OrderItem> orderItemList;
	
	public List<OrderItem> getOrderItemList() {
		return orderItemList;
	}
	public void setOrderItemList(List<OrderItem> orderItemList) {
		this.orderItemList = orderItemList;
	}
	public String getOid() {
		return oid;
	}
	public void setOid(String oid) {
		this.oid = oid;
	}
	public String getOrdertime() {
		return ordertime;
	}
	public void setOrdertime(String ordertime) {
		this.ordertime = ordertime;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public User getOwner() {
		return owner;
	}
	public void setOwner(User owner) {
		this.owner = owner;
	}
	
	
}
